package day15_ForLoop;

public class Alphabet {
    public static void main(String[] args) {

        for (char i='A'; i<='Z'; i++){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("------------------");

        for (char z='a'; z<='z'; z++){
            System.out.print(z+" ");
        }
        System.out.println();
        System.out.println("================================");

        for(char x=1;x<=40000;x++){
            System.out.print(x+" ");
        }












    }
}
